    private void addCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCategoryActionPerformed
        try {
            String nombre = JOptionPane.showInputDialog("Escribe el nombre de la nueva categoria a añadir: ").toUpperCase();

            boolean repetida = false;
            for (Categoria c : categoriasData){
                if (c.getNombre().equals(nombre)) {
                    repetida = true;
                    break;
                }
            }
            
            if (!repetida) {
                Categoria nueva = new Categoria();
                nueva.setNombre(nombre);
                boolean ok = categorias.agregarCategoria(nueva);

                if (ok) {
                    JOptionPane.showMessageDialog(this, "Categoria añadida", "", JOptionPane.INFORMATION_MESSAGE);
                    categoriasData = categorias.getTodasLasCategorias();
                    cmbCategorias.removeAllItems();
                    for (Categoria c : categoriasData)
                        cmbCategorias.addItem(c.getNombre());
                }
                else
                    JOptionPane.showMessageDialog(this, "No se pudo añadir la categoria", "¡ERROR!", JOptionPane.ERROR_MESSAGE);
            }
            else
                JOptionPane.showMessageDialog(this, "Ya existe una categoria con ese nombre", "¡ERROR!", JOptionPane.ERROR_MESSAGE);
        }
        catch (Exception e) {}
    }//GEN-LAST:event_addCategoryActionPerformed

