    private void calcularPrecios () {
        if (!txtPrecio.getText().equals("") && !txtDescuento.getText().equals("")) {
            Double precio = Double.parseDouble(txtPrecio.getText());

            if (rbtConIVA.isSelected()) {
                precioBASE = precio/(1.16);
                precioFINAL = precio;
                IVA = precioFINAL - precioBASE;
            }
            else {
                precioBASE = precio;
                IVA = precio*0.16;
                precioFINAL = precioBASE+IVA;
            }
            
            descuento = precioFINAL*(Double.parseDouble(txtDescuento.getText()))/100.0;
            
            lblPrecioBase.setText(String.format("%.2f", precioBASE));
            lblIVA.setText(String.format("%.2f", IVA));
            lblDescuento.setText(String.format("%.2f", descuento));
            lblPrecioFinal.setText(String.format("%.2f", precioFINAL-descuento));
        }
    }

