    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String clave = txtClave.getText().toUpperCase();
        String descripcion = txtDescripcion.getText();
        String marca = txtMarca.getText();
        
        String errorMessage = "FALTA INGRESAR:";
        if (clave.equals(""))
            errorMessage += "\n - Clave";
        if (descripcion.equals(""))
            errorMessage += "\n - Descripción";
        if (marca.equals(""))
            errorMessage += "\n - Marca";
        
        if (errorMessage.equals("FALTA INGRESAR:")) {
            Producto nuevo = new Producto();
            if (producto != null)
                nuevo.setId(producto.getId());
            nuevo.setCodigoBarras(txtCodigoBarras.getText());
            nuevo.setClave(clave);
            nuevo.setDescripcion(descripcion);
            nuevo.setMarca(marca);
            nuevo.setPrecio(Double.parseDouble(txtPrecio.getText()));
            nuevo.setDescuento(Integer.parseInt(txtDescuento.getText()));
            nuevo.setStock(Integer.parseInt(txtStock.getText()));
            int idCategoria = categorias.getId("" + cmbCategorias.getSelectedItem());
            nuevo.setIdCategoria(idCategoria);
            nuevo.setFoto(fotoProducto);

            boolean ok = productos.guardarProducto(nuevo);
            if (ok) {
                JOptionPane.showMessageDialog(this, "El producto se ha guardado", "", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                Inventario i = new Inventario(usuario);
                i.setVisible(true);
            }
            else
                JOptionPane.showMessageDialog(this, "El producto no se pudo guardar", "¡ERROR!", JOptionPane.ERROR_MESSAGE);
            
        }
        else
            JOptionPane.showMessageDialog(this, errorMessage, "", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnSubirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubirActionPerformed
        JFileChooser archivoSelector = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos de imagen", "jpg", "png");
        archivoSelector.setFileFilter(filtro);
        
        int resultado = archivoSelector.showOpenDialog(this);
        if (resultado == JFileChooser.APPROVE_OPTION) {
            File selectedFile = archivoSelector.getSelectedFile();
            ImageIcon imagenIcono = new ImageIcon(selectedFile.getAbsolutePath());
            Image imagen = imagenIcono.getImage();
            Image imagenRedimensionada = imagen.getScaledInstance(256, 256, Image.SCALE_SMOOTH);
            lblFoto.setIcon(new ImageIcon(imagenRedimensionada));
            
            try {
                fotoProducto = new FileInputStream (selectedFile);
                fotoSubida = true;
            } catch (FileNotFoundException ex) {
                Logger.getLogger(FormularioProducto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSubirActionPerformed

