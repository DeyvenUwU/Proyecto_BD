    public ticket(Empleado usuario, int idVenta) {
        config();
        
        this.usuario = usuario;
        
        Venta v = ventas.getVenta(idVenta);
        ArrayList<DetalleVenta> detalles = ventas.getTodosLosDetalles(idVenta);
        
        String s = "\t\t\t\tPARAISO RANGEL - YURIRIA\n"
                + "\t\t     Hidalgo #1000, Yuriria Centro, 38940 Yuriria, Gto.\n\n"
                + "TicketID:\t" + v.getId() + "\n"
                + "Atendi√≥:\t\t" + usuario.getNombre() + "\n"
                + "Fecha:\t\t" + v.getFecha() +"\n\n"
                + "----------------------------------------------------------------------------------------\n"
                + "#Cant.\tClave\tDescripcion\t\t\t Precio Unitario\t Importe\n"
                + "----------------------------------------------------------------------------------------\n";
        
        for (DetalleVenta dv : detalles) {
            String des = productos.getDescripcionProducto(dv.getIdProducto());
            s += (dv.getCantidad() + "\t");
            s += (dv.getIdProducto() + "\t");
            s += (des);
            for (int i=des.length(); i<28; i++)
                s += " ";
            String pre = String.format("%.2f",(dv.getPrecio()-dv.getDescuento()));
            for (int i=pre.length(); i<20; i++)
                s += " ";
            s += ("$ " + pre);
            String imp = String.format("%.2f",((dv.getCantidad()*dv.getPrecio())-(dv.getDescuento()*dv.getCantidad())));
            for (int i=imp.length(); i<20; i++)
                s += " ";
            s += ("$ " + imp);
            s += "\n";
        }
        
        
                s += "----------------------------------------------------------------------------------------\n"
                + "\t\t\t\t\t\t\t\tSubTotal:  $\t" + String.format("%.2f", v.getSubTotal()) + "\n"
                + "\t\t\t\t\t\t\t\tIVA (16%): $\t" + String.format("%.2f", v.getIva()) + "\n"
                + "\t\t\t\t\t\t\t\tDescuento: $\t" + String.format("%.2f", v.getDescuento()) + "\n\n"
                + "\t\t\t\t\t\t\t\tTOTAL:     $\t" + String.format("%.2f", v.getTotal()) + "\n\n\n"
                + "\t\t\t\tGRACIAS POR SU COMPRA :D";
        txtInfo.setText(s);
    }
    
